import { NextResponse } from "next/server";
import { openai } from "@/lib/openai";

type City = "astana" | "tashkent";
type CompanyType = "with_children" | "with_masha" | "with_friends" | "alone";
type Duration = "1_2_hours" | "2_3_hours";
type Budget = "econom" | "medium" | "premium";
type Mood = "fun" | "calm" | "beautiful" | "legendary" | "surprise";

function getSpecialCaseIdea(input: {
  city: City;
  companyType: CompanyType;
  duration: Duration;
  budget: Budget;
  mood: Mood;
}): string | null {
  const { city, companyType, duration, budget, mood } = input;
  const cityLabel = city === "astana" ? "Астана" : "Ташкент";
  const currency = city === "astana" ? "₸" : "сум";

  // playful, but practical, non-toxic
  if (mood === "surprise" && duration === "1_2_hours" && budget === "econom") {
    return [
      "Заголовок: Секретный мини‑праздник за 90 минут",
      "Атмосфера: тепло, смешно, без подготовки «на сутки».",
      `Бюджет: ${currency} в пределах «эконом» (доставка/десерт + мелочь на свечи/бумагу).`,
      "План:",
      "1) Купи/найди: свечи + открытку/лист бумаги (10 минут).",
      "2) Закажи доставку: десерт + что-то солёное. Попроси свечу/приборы в комментарии.",
      "3) Пока едет: напиши 6 коротких вопросов‑квестов (по 1 строке) и спрячь их по дому.",
      "4) Мини‑квест 10 минут → свеча → фото → чай/кофе.",
      "5) Финал: 2–3 коротких голосовых поздравления от близких по очереди.",
      "Где проверить/собрать:",
      `- Карты/2ГИС (${cityLabel}): «кондитерская доставка», «доставка еды» рядом`,
      "- Мессенджер/звонок: уточнить время доставки и свечу",
      "- Поиск: «плейлист день рождения спокойный/весёлый»",
      "Альтернатива:",
      "- Если доставка долго: ближайший магазин + готовый десерт/свеча."
    ].join("\n");
  }

  if (companyType === "with_children" && duration === "1_2_hours" && mood === "fun") {
    return [
      "Заголовок: Домашняя мини‑олимпиада + награды",
      "Атмосфера: шумно, мило, все заняты и смеются.",
      `Бюджет: ${currency} в рамках выбранного бюджета (снэки + наклейки/маленькие призы).`,
      "План:",
      "1) За 10 минут подготовь 4 испытания: самолётики, попадание в корзину, танец 30 сек, мини‑квиз.",
      "2) Раздай роли: ведущий/судья/фотограф (детям нравится).",
      "3) Таблица очков на листе + 1 «бонус‑раунд» для именинника.",
      "4) Перерыв: сок/фрукты/сэндвичи, свеча на десерте.",
      "5) Финал: награды (наклейки) + общий снимок.",
      "Где проверить/собрать:",
      "- Карты/2ГИС: «канцтовары», «шары», «кондитерская рядом»",
      "- Доставка: «десерт/пицца доставка»",
      "- Поиск: «наклейки медали купить рядом»",
      "Альтернатива:",
      "- Если мало сил: оставь 2 испытания и сделай упор на фото/видео."
    ].join("\n");
  }

  if (budget === "premium" && mood === "beautiful" && duration === "2_3_hours") {
    return [
      "Заголовок: Красивый вечер без суеты",
      "Атмосфера: спокойно, эстетично, без сложной логистики.",
      `Бюджет: ${currency} «можно красиво» (такси/трансфер + ужин/десерт + маленький сюрприз).`,
      "План:",
      "1) Выбери приятный маршрут для 20–30 минут прогулки и отметь 2 точки на карте.",
      "2) Забронируй стол (или закажи «ужин для двоих» доставкой). Попроси тихий стол и десерт со свечой.",
      "3) Маленький сюрприз: мини‑букет/свеча/подарок «в карман».",
      "4) Прогулка + 10 фото → ужин → десерт и короткие тосты (по 1 минуте).",
      "5) Финал: вместе написать 10 желаний на год и выбрать 1 «первый шаг» на завтра.",
      "Где проверить/собрать:",
      `- Карты/2ГИС (${cityLabel}): «ресторан», фильтр «тихо/романтично», сортировка по рейтингу`,
      "- Tripadvisor / Ultima: выбрать 3 варианта, потом звонок/WhatsApp для брони",
      "- Поиск: «ужин для двоих доставка» + «десерт со свечой»",
      "Альтернатива:",
      "- Если нет мест: заменить ужин на «десерт‑бар + прогулка» и часть бюджета оставить на подарок."
    ].join("\n");
  }

  return null;
}

export async function POST(req: Request) {
  try {
    const body = (await req.json().catch(() => ({}))) as {
      city?: City;
      companyType?: CompanyType;
      duration?: Duration;
      budget?: Budget;
      mood?: Mood;
    };

    const city = body.city ?? "astana";
    const companyType = body.companyType ?? "with_friends";
    const duration = body.duration ?? "1_2_hours";
    const budget = body.budget ?? "medium";
    const mood = body.mood ?? "beautiful";

    const cityLabel = city === "astana" ? "Астана" : "Ташкент";
    const companyLabel =
      companyType === "with_children"
        ? "с детьми"
        : companyType === "with_masha"
          ? "вдвоём с Машей"
          : companyType === "with_friends"
            ? "с друзьями"
            : "один";
    const durationLabel = duration === "1_2_hours" ? "1–2 часа" : "2–3 часа";
    const budgetLabel =
      budget === "econom" ? "экономно" : budget === "medium" ? "средне" : "можно красиво";
    const moodLabel =
      mood === "fun"
        ? "весело"
        : mood === "calm"
          ? "спокойно"
          : mood === "beautiful"
            ? "красиво"
            : mood === "legendary"
              ? "легендарно"
              : "удиви меня";

    const currency = city === "astana" ? "₸" : "сум";
    const budgetRange =
      city === "astana"
        ? budget === "econom"
          ? "15 000–35 000 ₸ на двоих (без алкоголя) или 8 000–18 000 ₸ на человека"
          : budget === "medium"
            ? "35 000–80 000 ₸ на двоих или 18 000–40 000 ₸ на человека"
            : "80 000–180 000 ₸ на двоих или 40 000–90 000 ₸ на человека"
        : budget === "econom"
          ? "250 000–600 000 сум на двоих или 120 000–300 000 сум на человека"
          : budget === "medium"
            ? "600 000–1 500 000 сум на двоих или 300 000–750 000 сум на человека"
            : "1 500 000–3 500 000 сум на двоих или 750 000–1 800 000 сум на человека";

    const specialCase = getSpecialCaseIdea({ city, companyType, duration, budget, mood });
    if (specialCase) return NextResponse.json({ ok: true, text: specialCase });

    const prompt = [
      "Сгенерируй ОДНУ идею празднования дня рождения. Язык: русский.",
      "Твоя цель — бытовой, реалистичный, быстро реализуемый сценарий. Никаких красивых фантазий.",
      "Стоп-лист: не предлагай «ужин на крыше», «ресторан с видом», «особенное пространство» и т.п., если за этим нет конкретного лёгкого плана.",
      "Предпочитай то, что собирается через обычный поиск, карту, звонок/сообщение, доставку и быстрые бронирования.",
      "Ты не можешь проверять афишу/расписание. НЕ выдумывай конкретные названия мест, даты, события, цены «до копейки».",
      "Если нужны места/услуги — пиши, ЧТО именно искать и КАК быстро проверить (звонок/WhatsApp/сайт/карты).",
      `Город: ${cityLabel}.`,
      `С кем: ${companyLabel}.`,
      `Длительность: ${durationLabel}.`,
      `Бюджет: ${budgetLabel}.`,
      `Настроение: ${moodLabel}.`,
      `Валюта: ${currency}.`,
      `Жёсткое ограничение по бюджету (ориентир): ${budgetRange}.`,
      "Правила реализма:",
      "- Бюджет должен быть правдоподобным для города и формата (sanity-check).",
      "- Если идея выходит за бюджет — упрощай шаги, заменяй активности на более дешёвые, но не нарушай ограничение.",
      "- Пиши коротко, плотно, только реальные действия.",
      "Формат ответа строго:",
      "Заголовок: 4–7 слов (без пафоса)",
      "Атмосфера: 1 строка (очень конкретно)",
      "Бюджет: диапазон + что включено (1 строка, правдоподобно)",
      "План (4–6 шагов):",
      "1) …",
      "2) …",
      "…",
      "Где проверить/собрать (3 пункта):",
      "- Карты/2ГИС: запрос «…» + фильтр «…»",
      "- Афиша: Яндекс Афиша / afisha.uz / iticket.uz: запрос «…»",
      "- Мессенджер/звонок: что уточнить за 60 секунд",
      "Альтернатива (1–2 коротких пункта):",
      "- …",
      "Без эмодзи. Без лишних вступлений."
    ].join("\n");

    const completion = await openai().chat.completions.create({
      model: "gpt-4o-mini",
      temperature: 0.45,
      messages: [
        {
          role: "system",
          content:
            "Ты — практичный продюсер праздника. Дай один бытовой, быстро реализуемый сценарий под условия. Не выдумывай названия мест/события/даты, давай конкретные действия и правдоподобный бюджет. Без эмодзи."
        },
        { role: "user", content: prompt }
      ]
    });

    const text = completion.choices[0]?.message?.content?.trim() ?? "";
    if (!text) {
      return NextResponse.json(
        { ok: false, error: "Пустой ответ от модели." },
        { status: 502 }
      );
    }

    return NextResponse.json({ ok: true, text });
  } catch (e) {
    return NextResponse.json(
      { ok: false, error: e instanceof Error ? e.message : String(e) },
      { status: 500 }
    );
  }
}

